''' \n = new / next line
 \t = tab
 \\ = backslash
 \' = insert a single quote inside a single - quoted string 
 '''
 
 # \n
 
print("Hello everyone.\nHow are you?")

# \t

print("vivek 20")
print("vivek\t20")

# \\

print("new\old")
print("yes\\no")

# \'

# print('This is a python's class')
print("This is a python\'s class")
print("This is a python's class")


# \"

# print("This is a python"s class")
print("he said \"hii\"")
print('This is a python"s class')


#\r 

print("hel\rlo world")

# \b
print("hello\bworld")

# \f

print("hello\fwor\fld")