'''
while loop is also called as condition based loop

while loop :- It will keep executing the statements written inside the while block until the condition is true
                and when it becomes false then it will comes out of the loop

synatx :- 

while condition :
    statements
'''

for i in range(1,5) :
    print(i)
    
num = 1
while num < 5 :
    print(num)
    num = num + 1
    